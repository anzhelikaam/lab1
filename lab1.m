>> % Лабораторна робота №1
% Варіант 3 (Частина 1) - Класифікація двовимірних образів

clear; clc; close all;

% Навчальні дані
X = [-1.5  1.5  -0.5   2.0;
      1.5  2.0   0.3   1.0];     % Вхідні вектори (дві координати)
T = [0 0 1 1];                   % Відповідні класи

% Створення та налаштування персептрона
net = perceptron;
net = configure(net, X, T);
net.trainFcn = 'trainc';         % Метод корекції помилки

% Навчання персептрона
net = train(net, X, T);

% Візуалізація класифікації
figure;
plotpv(X, T);
plotpc(net.IW{1,1}, net.b{1});
title('Класифікація двовимірних образів (Частина 1)');
grid on;

%% Частина 2 – Класифікація атлетів (Варіант 2)
